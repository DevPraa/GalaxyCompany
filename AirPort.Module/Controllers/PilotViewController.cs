        ReportServiceController controller;
        private void PilotViewController_Activated(object sender, EventArgs e)
        {
            IObjectSpace objectSpace =
    ReportDataProvider.ReportObjectSpaceProvider.CreateObjectSpace(typeof(ReportDataV2));
            IReportDataV2 reportData =
                objectSpace.FindObject<ReportDataV2>(
                CriteriaOperator.Parse("[DisplayName] = 'Pilot Report'"));
            handle = ReportDataProvider.ReportsStorage.GetReportContainerHandle(reportData);
